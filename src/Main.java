import java.util.Random;
public class Main {
    public static void main(String[] args) {
        // 1. Оголошення змінних
        long l = 123456L;        // змінна типу long (8 байтів)
        double d1, d2;           // змінні типу double (8 байтів)
        float f = 14.75f;        // змінна типу float (4 байти)
        char c1 = '0';           // змінна типу char (2 байти)

        // Виведемо початкові значення змінних
        System.out.println("Початкове значення змінної l (тип long, 8 байтів): " + l);
        System.out.println("Початкове значення змінної f (тип float, 4 байти): " + f);
        System.out.println("Початкове значення змінної c1 (тип char): " + c1);

        // 2. Виконання операцій зі змінними

        // 2.1 Змінній l привласнити значення змінної f
        l = (long) f;  // Перетворення float в long (втрачення дробової частини)
        System.out.println("Значення змінної l після присвоєння f (float перетворено на long, дробова частина втрачена): " + l);

        // 2.2 Присвоєння випадкового значення змінній d1 в діапазоні від 100 до 1000
        Random rand = new Random();
        d1 = 100 + (900 * rand.nextDouble()); // Генерація випадкового числа в діапазоні [100, 1000]
        System.out.println("Випадкове значення змінної d1 (тип double, в діапазоні [100, 1000]): " + d1);

        // 2.3 Присвоєння випадкового значення змінній d2 в діапазоні від 1 до 99
        d2 = 1 + (99 * rand.nextDouble()); // Генерація випадкового числа в діапазоні [1, 99]
        System.out.println("Випадкове значення змінної d2 (тип double, в діапазоні [1, 99]): " + d2);
        // 3. Обчислення виразу y = (√d1 - 3√d2) / ((l - d1) * (l^2 + d2))
        double y = (Math.sqrt(d1) - 3 * Math.sqrt(d2)) / ((l - d1) * (Math.pow(l, 2) + d2));

        // Виведення результату
        System.out.println("Значення виразу y: " + y);
    }
}
